# # About the HOWTOs
#
# ## Scope
#
# These HOWTOs are introducing the different possibilities of working with the flodym package, ordered by topic.
# They may be good for a first overview. For reference during implementation, please refer to the API documentation.
# For more application-oriented examples, please refer to the examples folder.
# They are more compact examples of whole MFA's dealing with actual research questions,
# but less structured and comprehensive in terms of alternative ways of flodym usage.
#
# ## flodym Design: Different levels of integration
#
# The flodym packages is designed both for flexibility and convenience.
# Since this is often a contradiction, it offers different levels of integration.
# For example, you can use the flodym data read-in and system-setup routines, or you can create the necessary objects yourself and pass them to the class constructor.
#
# The following HOWTOs will outline the different ways to initialize and work with different flodym objects,
# either in convenient pre-coded or more customized ways.
#
